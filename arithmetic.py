def add(x, y):
    """Return x + y"""

    return x + y


def subtract(x, y):
    """Return x - y"""

    return x - y 


def multiply(x, y):
    """Return x * y"""

    return x * y


def divide(x, y):
    """Return x / y"""

    return float(x) / y


def square(x):
    """Return x to the second power"""

    return x**x


def cube(x):
    """Return x cubed"""

    return x**3 


def power(x, y):
    """Return x to the power of y"""

    return x**y


def mod(x, y):
    """Return modulus of x divided by y"""

    return x % y